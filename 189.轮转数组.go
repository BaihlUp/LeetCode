// @lc code=start
func rotate(nums []int, k int) {
	k = k % len(nums)
	revers(nums)
	revers(nums[:k])
	revers(nums[k:])
}

func revers(a []int) {
	i, j := 0, len(a)-1
	for i < j {
		a[i], a[j] = a[j], a[i]
		i++
		j--
	}
}

